package Filter;

public class Produto {
	
	final String nome;
	final double preco;
	final double desconto;
	final int valorFrete;
	
	public Produto(String nome, double preco, double desconto, int valorFrete) {
		this.nome = nome;
		this.preco = preco;
		this.desconto = desconto;
		this.valorFrete = valorFrete;
	}
	
	
	

}
